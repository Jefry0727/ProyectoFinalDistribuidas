public class Tarjeta {
	
	 private String ciudadTarjeta;
	 private String codigoSeguridad;
	 private String fechaVencimiento;
	 private String metodoPago; 
	 private String nombreTarjeta;
	 private String numeroTarjeta;
	 private int valor;
	/**
	 * @return the ciudadTarjeta
	 */
	public String getCiudadTarjeta() {
		return ciudadTarjeta;
	}
	/**
	 * @param ciudadTarjeta the ciudadTarjeta to set
	 */
	public void setCiudadTarjeta(String ciudadTarjeta) {
		this.ciudadTarjeta = ciudadTarjeta;
	}
	/**
	 * @return the codigoSeguridad
	 */
	public String getCodigoSeguridad() {
		return codigoSeguridad;
	}
	/**
	 * @param codigoSeguridad the codigoSeguridad to set
	 */
	public void setCodigoSeguridad(String codigoSeguridad) {
		this.codigoSeguridad = codigoSeguridad;
	}
	/**
	 * @return the fechaVencimiento
	 */
	public String getFechaVencimiento() {
		return fechaVencimiento;
	}
	/**
	 * @param fechaVencimiento the fechaVencimiento to set
	 */
	public void setFechaVencimiento(String fechaVencimiento) {
		this.fechaVencimiento = fechaVencimiento;
	}
	/**
	 * @return the metodoPago
	 */
	public String getMetodoPago() {
		return metodoPago;
	}
	/**
	 * @param metodoPago the metodoPago to set
	 */
	public void setMetodoPago(String metodoPago) {
		this.metodoPago = metodoPago;
	}
	/**
	 * @return the nombreTarjeta
	 */
	public String getNombreTarjeta() {
		return nombreTarjeta;
	}
	/**
	 * @param nombreTarjeta the nombreTarjeta to set
	 */
	public void setNombreTarjeta(String nombreTarjeta) {
		this.nombreTarjeta = nombreTarjeta;
	}
	/**
	 * @return the numeroTarjeta
	 */
	public String getNumeroTarjeta() {
		return numeroTarjeta;
	}
	/**
	 * @param numeroTarjeta the numeroTarjeta to set
	 */
	public void setNumeroTarjeta(String numeroTarjeta) {
		this.numeroTarjeta = numeroTarjeta;
	}
	/**
	 * @return the valor
	 */
	public int getValor() {
		return valor;
	}
	/**
	 * @param valor the valor to set
	 */
	public void setValor(int valor) {
		this.valor = valor;
	}
	 
	 

}
