public class Producto {
	
	private String id;
	private String nombre;
	private String marca;
	private String titulo;
	private String descripcion;
	private String imagen_pequenia;
	private String imagen_media;
	private String imagen_grande;
	private String total;
	private double valor;
	/**
	 * @return the id
	 */
	public String getId() {
		return id;
	}
	/**
	 * @param id the id to set
	 */
	public void setId(String id) {
		this.id = id;
	}
	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}
	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	/**
	 * @return the marca
	 */
	public String getMarca() {
		return marca;
	}
	/**
	 * @param marca the marca to set
	 */
	public void setMarca(String marca) {
		this.marca = marca;
	}
	/**
	 * @return the titulo
	 */
	public String getTitulo() {
		return titulo;
	}
	/**
	 * @param titulo the titulo to set
	 */
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	/**
	 * @return the descripcion
	 */
	public String getDescripcion() {
		return descripcion;
	}
	/**
	 * @param descripcion the descripcion to set
	 */
	public void setDescripcion(String descripcion) {
		this.descripcion = descripcion;
	}
	/**
	 * @return the imagen_pequenia
	 */
	public String getImagen_pequenia() {
		return imagen_pequenia;
	}
	/**
	 * @param imagen_pequenia the imagen_pequenia to set
	 */
	public void setImagen_pequenia(String imagen_pequenia) {
		this.imagen_pequenia = imagen_pequenia;
	}
	/**
	 * @return the imagen_media
	 */
	public String getImagen_media() {
		return imagen_media;
	}
	/**
	 * @param imagen_media the imagen_media to set
	 */
	public void setImagen_media(String imagen_media) {
		this.imagen_media = imagen_media;
	}
	/**
	 * @return the imagen_grande
	 */
	public String getImagen_grande() {
		return imagen_grande;
	}
	/**
	 * @param imagen_grande the imagen_grande to set
	 */
	public void setImagen_grande(String imagen_grande) {
		this.imagen_grande = imagen_grande;
	}
	/**
	 * @return the valor
	 */
	public double getValor() {
		return valor;
	}
	/**
	 * @param valor the valor to set
	 */
	public void setValor(double valor) {
		this.valor = valor;
	}
	/**
	 * @return the total
	 */
	public String getTotal() {
		return total;
	}
	/**
	 * @param total the total to set
	 */
	public void setTotal(String total) {
		this.total = total;
	}
	
	
	
}
